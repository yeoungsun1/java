package print;

public class PrintTest01 { // 클래스 시작 중괄호
	public static void main(String[] args) { //메인메소드 시작 중괄호
		
		//출력메소드 : ()소괄호안에 있는 값을 콘솔창에 출력한다
//		println() : 소괄호안에 있는 값을 콘솔창에 출력하고 줄바꿈이 된다
//		print()  : 소괄호안에 있는 값을 콘솔창에 출력하고 줄바꿈이 되지 않는다
		
		//출력 메소드  syso + ctrl + space bar 엔터
		System.out.println("김영선");
		System.out.println("김영선");
		System.out.println("김영선");
		System.out.println("김영선");
		System.out.print("10 + 20\n"); // "" 문자열을 의미, 10 + 20 문자열로 되어 그대로 출력되고 줄바꿈이 된다
		System.out.println(10 + 20); //연산된 결과 30이 출력되고 줄바꿈이 된다
		//저장  	ctrl + s
		//실행	ctrl + F11
		//이동	alt + 방향키 위아래
		//복사	ctrl + alt + 방향키 위아래
	} //메인메소드 종료 중괄호
} // 클래스 종료 중괄호
